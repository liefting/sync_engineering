function h_functions = get_h_functions(al, omega)

    a1=al(1); a2=al(2); a3=al(3); a4=al(4); a5=al(5);
    a_1=conj(a1); a_2=conj(a2); a_3=conj(a3); a_4=conj(a4); a_5=conj(5);
       
    h_functions.h100 = @(fb) fb.k100*exp(omega*fb.tau100*1i)*a1 ... 
            + 2*fb.k200*exp(omega*fb.tau200*1i)*( a2*a_1 + a3*a_2 + a4*a_3 + a5*a_4 )...
            + 2*( fb.k120*exp(1i*omega*fb.tau120) + fb.k102*exp(1i*omega*fb.tau102) )...
              * ( a1*a_5*a5 + a1*a_4*a4 + a1*a_3*a3 + a1*a_2*a2 + a1*a_1*a1  ) ...
            + fb.k300*exp(1i*omega*fb.tau300)*( 3*a1^2*a_1 + 6*a1*a2*a_2 + 6*a1*a3*a_3 ...
                + 6*a1*a4*a_4 + 6*a1*a5*a_5 ...
                + 3*a2^2*a_3 + 6*a2*a3*a_4 + 6*a2*a4*a_5...
                + 3*a3*a_1^2 + 3*a3^2*a_5 ...           
                + 6*a4*a_1*a_2 + 3*a5*a_2^2 + 6*a5*a_3*a_1 );     
     h_functions.h010 = @(fb) fb.k010*exp(omega*fb.tau010*1i)*a1 ... 
            + 2*fb.k020*exp(omega*fb.tau020*1i)*( a2*a_1 + a3*a_2 + a4*a_3 + a5*a_4 )...
            + 2*( fb.k210*exp(1i*omega*fb.tau210) + fb.k012*exp(1i*omega*fb.tau012) )...
              * ( a1*a_5*a5 + a1*a_4*a4 + a1*a_3*a3 + a1*a_2*a2 + a1*a_1*a1  ) ...
            + fb.k030*exp(1i*omega*fb.tau030)*( 3*a1^2*a_1 + 6*a1*a2*a_2 + 6*a1*a3*a_3 ...
                + 6*a1*a4*a_4 + 6*a1*a5*a_5 ...
                + 3*a2^2*a_3 + 6*a2*a3*a_4 + 6*a2*a4*a_5...
                + 3*a3*a_1^2 + 3*a3^2*a_5 ...           
                + 6*a4*a_1*a_2 + 3*a5*a_2^2 + 6*a5*a_3*a_1 );  
      h_functions.h001 = @(fb) fb.k001*exp(omega*fb.tau001*1i)*a1 ... 
            + 2*fb.k002*exp(omega*fb.tau002*1i)*( a2*a_1 + a3*a_2 + a4*a_3 + a5*a_4 )...
            + 2*( fb.k201*exp(1i*omega*fb.tau201) + fb.k021*exp(1i*omega*fb.tau021) )...
              * ( a1*a_5*a5 + a1*a_4*a4 + a1*a_3*a3 + a1*a_2*a2 + a1*a_1*a1  ) ...
            + fb.k003*exp(1i*omega*fb.tau003)*( 3*a1^2*a_1 + 6*a1*a2*a_2 + 6*a1*a3*a_3 ...
                + 6*a1*a4*a_4 + 6*a1*a5*a_5 ...
                + 3*a2^2*a_3 + 6*a2*a3*a_4 + 6*a2*a4*a_5...
                + 3*a3*a_1^2 + 3*a3^2*a_5 ...       
                + 6*a4*a_1*a_2 + 3*a5*a_2^2 + 6*a5*a_3*a_1 );  

    h_functions.h110 = @(fb) fb.k110*a1^2*exp(1i*omega*(fb.tau110)) ...
            +2*(fb.k210*exp(2i*omega*fb.tau210) + fb.k120*exp(2i*omega*fb.tau120))*...
                (a1*a2*a_1 + a1*a3*a_2 + a1*a4*a_3 + a1*a5*a_4);
    h_functions.h011 = @(fb) fb.k011*a1^2*exp(1i*omega*(fb.tau011)) ...
            +2*(fb.k021*exp(2i*omega*fb.tau021) + fb.k012*exp(2i*omega*fb.tau012))*...
                (a1*a2*a_1 + a1*a3*a_2 + a1*a4*a_3 + a1*a5*a_4);
    h_functions.h101 = @(fb) fb.k101*a1^2*exp(1i*omega*(fb.tau101)) ...
            +2*(fb.k201*exp(2i*omega*fb.tau201) + fb.k102*exp(2i*omega*fb.tau102))*...
                (a1*a2*a_1 + a1*a3*a_2 + a1*a4*a_3 + a1*a5*a_4);
    
    h_functions.h200 = @(fb) fb.k100*exp(2i*omega*fb.tau100)*a2 ... % fix 21/7
            + fb.k200*exp(2i*omega*fb.tau200)*(a1^2 + 2*a3*a_1 + 2*a4*a_2 + 2*a5*a_3 )...
            + 2*( fb.k120*exp(2i*omega*fb.tau120) + fb.k102*exp(2i*omega*fb.tau102) )...
              * ( a2*a_5*a5 + a2*a_4*a4 + a2*a_3*a3 + a2*a_2*a2 + a2*a_1*a1  ) ...
            + fb.k300*exp(2i*omega*fb.tau300)*(6*a1*a2*a_1 + 6*a1*a3*a_2 + 6*a1*a4*a_3 + 6*a1*a5*a_4 ...
                    + 3*a2^2*a_2 + 6*a2*a3*a_3 + 6*a2*a4*a_4 + 6*a2*a5*a_5 ...
                    + 3*a3^2*a_4 + 6*a3*a4*a_5 + 3*a4*a_1^2 + 6*a5*a_1*a_2   );    
    h_functions.h020 = @(fb) fb.k010*exp(2i*omega*fb.tau010)*a2 ... % fix 21/7
            + fb.k020*exp(2i*omega*fb.tau020)*(a1^2 + 2*a3*a_1 + 2*a4*a_2 + 2*a5*a_3 )...
            + 2*( fb.k210*exp(2i*omega*fb.tau210) + fb.k012*exp(2i*omega*fb.tau012) )...
              * ( a2*a_5*a5 + a2*a_4*a4 + a2*a_3*a3 + a2*a_2*a2 + a2*a_1*a1  )...
            + fb.k030*exp(2i*omega*fb.tau030)*(6*a1*a2*a_1 + 6*a1*a3*a_2 + 6*a1*a4*a_3 + 6*a1*a5*a_4 ...
                    + 3*a2^2*a_2 + 6*a2*a3*a_3 + 6*a2*a4*a_4 + 6*a2*a5*a_5 ...
                    + 3*a3^2*a_4 + 6*a3*a4*a_5 + 3*a4*a_1^2 + 6*a5*a_1*a_2   );  
    h_functions.h002 = @(fb) fb.k001*exp(2i*omega*fb.tau001)*a2 ... % fix 21/7
            + fb.k002*exp(2i*omega*fb.tau002)*(a1^2 + 2*a3*a_1 + 2*a4*a_2 + 2*a5*a_3 )...
            + 2*( fb.k201*exp(2i*omega*fb.tau201) + fb.k021*exp(2i*omega*fb.tau021) )...
              * ( a2*a_5*a5 + a2*a_4*a4 + a2*a_3*a3 + a2*a_2*a2 + a2*a_1*a1  ) ...
            + fb.k003*exp(2i*omega*fb.tau003)*(6*a1*a2*a_1 + 6*a1*a3*a_2 + 6*a1*a4*a_3 + 6*a1*a5*a_4 ...
                    + 3*a2^2*a_2 + 6*a2*a3*a_3 + 6*a2*a4*a_4 + 6*a2*a5*a_5 ...
                    + 3*a3^2*a_4 + 6*a3*a4*a_5 + 3*a4*a_1^2 + 6*a5*a_1*a_2   );  

    h_functions.h2_10 = @(fb) fb.k110*a2*a_1*exp(1i*omega*(2*fb.tau110)) ...
         + 2*fb.k210*exp(1i*omega*fb.tau210)*(a_1*a1^2 + 2*a_1^2*a3 + 2*a_1*a4*a_2 + a_1*a5*a_3)...  
         + 2*fb.k120*exp(1i*omega*fb.tau120)*(2*a2*a1*a_2 + 2*a2^2*a_3 + 2*a2*a3*a_4 + 2*a2*a4*a_5);
    h_functions.h20_1 = @(fb) fb.k101*a2*a_1*exp(1i*omega*(2*fb.tau101)) ...
         + 2*fb.k201*exp(1i*omega*fb.tau201)*(a_1*a1^2 + 2*a_1^2*a3 + 2*a_1*a4*a_2 + a_1*a5*a_3)... 
         + 2*fb.k102*exp(1i*omega*fb.tau102)*(2*a2*a1*a_2 + 2*a2^2*a_3 + 2*a2*a3*a_4 + 2*a2*a4*a_5);
    h_functions.h02_1 = @(fb) fb.k011*a2*a_1*exp(1i*omega*(2*fb.tau011)) ...
         + 2*fb.k021*exp(1i*omega*fb.tau021)*(a_1*a1^2 + 2*a_1^2*a3 + 2*a_1*a4*a_2 + a_1*a5*a_3)... 
         + 2*fb.k012*exp(1i*omega*fb.tau012)*(2*a2*a1*a_2 + 2*a2^2*a_3 + 2*a2*a3*a_4 + 2*a2*a4*a_5);
    h_functions.h_120 = @(fb) fb.k110*a2*a_1*exp(1i*omega*(2*fb.tau110)) ...
         + 2*fb.k120*exp(1i*omega*fb.tau120)*(a_1*a1^2 + 2*a_1^2*a3 + 2*a_1*a4*a_2 + a_1*a5*a_3)...  
         + 2*fb.k210*exp(1i*omega*fb.tau210)*(2*a2*a1*a_2 + 2*a2^2*a_3 + 2*a2*a3*a_4 + 2*a2*a4*a_5);
    h_functions.h0_12 = @(fb) fb.k011*a2*a_1*exp(1i*omega*(2*fb.tau011)) ...
         + 2*fb.k012*exp(1i*omega*fb.tau012)*(a_1*a1^2 + 2*a_1^2*a3 + 2*a_1*a4*a_2 + a_1*a5*a_3)... 
         + 2*fb.k021*exp(1i*omega*fb.tau021)*(2*a2*a1*a_2 + 2*a2^2*a_3 + 2*a2*a3*a_4 + 2*a2*a4*a_5);
    h_functions.h_102 = @(fb) fb.k101*a2*a_1*exp(1i*omega*(2*fb.tau101)) ...
         + 2*fb.k102*exp(1i*omega*fb.tau102)*(a_1*a1^2 + 2*a_1^2*a3 + 2*a_1*a4*a_2 + a_1*a5*a_3)... 
         + 2*fb.k201*exp(1i*omega*fb.tau201)*(2*a2*a1*a_2 + 2*a2^2*a_3 + 2*a2*a3*a_4 + 2*a2*a4*a_5);
          
    h_functions.h11_1 = @(fb) fb.k111*exp(1i*omega*fb.tau111)*a1*a1*a_1; 

    h_functions.h111 = @(fb) fb.k111*exp(3i*omega*fb.tau111)*a1^3;  

    h_functions.h1_10 = @(fb) fb.k110*a1*a_1 ...
                            + 2*fb.k210*( a5*a_4*a1 + a4*a_3*a1 + a3*a_2*a1 + a2*a_1*a1) ...
                            + 2*fb.k120*( a_5*a4*a1 + a_4*a3*a1 + a_3*a2*a1 + a_2*a1*a1 );

    h_functions.h10_1 = @(fb) fb.k101*a1*a_1 ...
                            + 2*fb.k201*( a5*a_4*a1 + a4*a_3*a1 + a3*a_2*a1 + a2*a_1*a1) ...
                            + 2*fb.k102*( a_5*a4*a1 + a_4*a3*a1 + a_3*a2*a1 + a_2*a1*a1 );

    h_functions.h01_1 = @(fb) fb.k011*a1*a_1 ...
                            + 2*fb.k021*( a5*a_4*a1 + a4*a_3*a1 + a3*a_2*a1 + a2*a_1*a1) ...
                            + 2*fb.k012*( a_5*a4*a1 + a_4*a3*a1 + a_3*a2*a1 + a_2*a1*a1 );

    h_functions.h300 = @(fb) fb.k100*exp(3i*omega*fb.tau100)*a3 ...
        + 2*fb.k200*exp(3i*omega*fb.tau200)*(a1*a2 + a4*a_1 + a5*a_2) ...
        + fb.k300*exp(3i*omega*fb.tau300)*(a1^3 + 6*a1*a3*a_1 + 6*a1*a4*a_2 + 6*a1*a5*a_3...
        + 3*a2^2*a_1 + 6*a2*a3*a_2 + 6*a2*a4*a_3 + 6*a2*a5*a_4...
        + 3*a3^2*a_3  + 6*a3*a4*a_4 + 6*a3*a5*a_5...
        + 3*a5*a_1^2);
    h_functions.h030 = @(fb) fb.k010*exp(3i*omega*fb.tau010)*a3 ...
        + 2*fb.k020*exp(3i*omega*fb.tau020)*(a1*a2 + a4*a_1 + a5*a_2) ...
        + fb.k030*exp(3i*omega*fb.tau030)*(a1^3 + 6*a1*a3*a_1 + 6*a1*a4*a_2 + 6*a1*a5*a_3...
        + 3*a2^2*a_1 + 6*a2*a3*a_2 + 6*a2*a4*a_3 + 6*a2*a5*a_4...
        + 3*a3^2*a_3  + 6*a3*a4*a_4 + 6*a3*a5*a_5...
        + 3*a5*a_1^2);
    h_functions.h003 = @(fb) fb.k001*exp(3i*omega*fb.tau001)*a3 ...
        + 2*fb.k002*exp(3i*omega*fb.tau002)*(a1*a2 + a4*a_1 + a5*a_2) ...
        + fb.k003*exp(3i*omega*fb.tau003)*(a1^3 + 6*a1*a3*a_1 + 6*a1*a4*a_2 + 6*a1*a5*a_3...
        + 3*a2^2*a_1 + 6*a2*a3*a_2 + 6*a2*a4*a_3 + 6*a2*a5*a_4...
        + 3*a3^2*a_3  + 6*a3*a4*a_4 + 6*a3*a5*a_5...
        + 3*a5*a_1^2);

    h_functions.h210 = @(fb) fb.k110*exp(3i*omega*fb.tau110)*a2*a1...
        + fb.k210*exp(3i*omega*fb.tau210)*(a1^3 + 2*a1*a3*a_1 + 2*a1*a4*a_2 + 2*a1*a5*a_3) ...
        + fb.k120*exp(3i*omega*fb.tau120)*(a1^3 + 2*a1*a3*a_1 + 2*a1*a4*a_2 + 2*a1*a5*a_3);
    h_functions.h201 = @(fb) fb.k101*exp(3i*omega*fb.tau101)*a2*a1...
        + fb.k201*exp(3i*omega*fb.tau201)*(a1^3 + 2*a1*a3*a_1 + 2*a1*a4*a_2 + 2*a1*a5*a_3) ...
        + fb.k102*exp(3i*omega*fb.tau102)*(a1^3 + 2*a1*a3*a_1 + 2*a1*a4*a_2 + 2*a1*a5*a_3);
    h_functions.h021 = @(fb) fb.k011*exp(3i*omega*fb.tau011)*a2*a1...
        + fb.k021*exp(3i*omega*fb.tau021)*(a1^3 + 2*a1*a3*a_1 + 2*a1*a4*a_2 + 2*a1*a5*a_3) ...
        + fb.k012*exp(3i*omega*fb.tau012)*(a1^3 + 2*a1*a3*a_1 + 2*a1*a4*a_2 + 2*a1*a5*a_3);
    h_functions.h120 = @(fb) fb.k110*exp(3i*omega*fb.tau110)*a2*a1...
        + fb.k120*exp(3i*omega*fb.tau120)*(a1^3 + 2*a1*a3*a_1 + 2*a1*a4*a_2 + 2*a1*a5*a_3) ...
        + fb.k210*exp(3i*omega*fb.tau210)*(a1^3 + 2*a1*a3*a_1 + 2*a1*a4*a_2 + 2*a1*a5*a_3);
    h_functions.h012 = @(fb) fb.k011*exp(3i*omega*fb.tau011)*a2*a1...
        + fb.k012*exp(3i*omega*fb.tau012)*(a1^3 + 2*a1*a3*a_1 + 2*a1*a4*a_2 + 2*a1*a5*a_3) ...
        + fb.k021*exp(3i*omega*fb.tau021)*(a1^3 + 2*a1*a3*a_1 + 2*a1*a4*a_2 + 2*a1*a5*a_3);
    h_functions.h102 = @(fb) fb.k101*exp(3i*omega*fb.tau101)*a2*a1...
        + fb.k102*exp(3i*omega*fb.tau102)*(a1^3 + 2*a1*a3*a_1 + 2*a1*a4*a_2 + 2*a1*a5*a_3) ...
        + fb.k201*exp(3i*omega*fb.tau201)*(a1^3 + 2*a1*a3*a_1 + 2*a1*a4*a_2 + 2*a1*a5*a_3);

end